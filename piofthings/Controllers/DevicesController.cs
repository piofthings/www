using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
//using Piofthings.Data.Contexts;
using Piofthings.Domain.Models.Devices;
using Piofthings.Domain.Interfaces;

namespace Piofthings.Web.Controllers
{
    [Authorize]
    public class DevicesController : Controller
    {
        readonly IDeviceServices _deviceServices;

        public DevicesController(IDeviceServices deviceService)
        {
            _deviceServices = deviceService;
        }
        

        // GET: Devices
        public ActionResult Index()
        {
            return View(_deviceServices.GetAllDevices(User.Identity.Name));
        }

        // GET: Devices/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Device device = _deviceServices.GetDevice(id.Value, User.Identity.Name);
            if (device == null)
            {
                return HttpNotFound();
            }
            return View(device);
        }

        // GET: Devices/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: Devices/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "Id,Identifier")] Device device)
        {
            device.Account = User.Identity.Name;
            if (ModelState.IsValid)
            {
                _deviceServices.SaveDevice(device);

                return RedirectToAction("Index");
            }

            return View(device);
        }

        // GET: Devices/Edit/5
        public ActionResult Edit(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Device device = _deviceServices.GetDevice(id.Value, User.Identity.Name);
            if (device == null)
            {
                return HttpNotFound();
            }
            return View(device);
        }

        // POST: Devices/Edit/5
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Edit([Bind(Include = "Id,Identifier,Name")] Device device)
        {
            if (ModelState.IsValid)
            {
                device.Account = User.Identity.Name;

                return RedirectToAction("Index");
            }
            return View(device);
        }

        // GET: Devices/Delete/5
        public ActionResult Delete(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Device device = _deviceServices.GetDevice(id.Value, User.Identity.Name);
            if (device == null)
            {
                return HttpNotFound();
            }
            return View(device);
        }

        // POST: Devices/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public ActionResult DeleteConfirmed(int id)
        {
            _deviceServices.DeleteDevice(id, User.Identity.Name);
            return RedirectToAction("Index");
        }

